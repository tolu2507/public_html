<script>
    document.addEventListener("DOMContentLoaded", function() {
        const tradingTabs = document.querySelectorAll(".trading-content-container");

        const observer = new IntersectionObserver(entries => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    // Remove 'visible' from all tabs before adding it to the current one
                    tradingTabs.forEach(tab => tab.classList.remove("visible"));
                    entry.target.classList.add("visible");
                }
            });
        }, {
            threshold: 0.6
        }); // Adjusted to 60% visibility to ensure only one is active

        tradingTabs.forEach(tab => observer.observe(tab));
    });
</script>